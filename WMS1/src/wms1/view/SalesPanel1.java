    public void showTableData() {
        Object[][] data = null;
        double tm = 0;
        double td = 0;
        double tfa = 0;
        if (salesItems != null && salesItems.size() > 0) {
            data = new Object[salesItems.size()][8];
            int i = 0;
            for (SalesItems p : salesItems) {
                data[i][0] = p.getSrNo();
                data[i][1] = p.getItems().getBrandId().getBrandName();
                data[i][2] = p.getItems().getItemName();
                data[i][3] = p.getQuantity();
                data[i][4] = p.getPrice();
                data[i][5] = p.getAmount();
                data[i][6] = p.getDiscount();
                data[i][7] = p.getTotalAmount();
                tm += p.getAmount();
                td += p.getDiscount();
                tfa += p.getTotalAmount();
                i++;
            }
        } else {
            data = new Object[0][0];
        }
        txtTotalAmt.setText(tm + "");
        txtTotalDisc.setText(td + "");
        txttotalFinalAmount.setText(tfa + "");
        tbPurItems.setModel(new javax.swing.table.DefaultTableModel(
                data,
                new String[]{
                    "Sr,No", "Brand", "Item", "Qty.", "Price", "Amount", "Discount", "Final Amt"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.Integer.class, java.lang.String.class, java.lang.String.class, java.lang.Integer.class, java.lang.Double.class, java.lang.Double.class, java.lang.Double.class, java.lang.Double.class
            };
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });

    }

