    public void searchItem() {
        boolean check = true;
        if ((txtBarCode.getText() == null || txtBarCode.getText().trim().length() == 0) && (ddItems.getSelectedItem() == null || ddItems.getSelectedItem().toString().equalsIgnoreCase("select"))) {
            check = false;
            JOptionPane.showMessageDialog(this, "Please select Item or enter bar code");
        }
        if (check) {
            long itId = 0;
            String barcode = null;
            if (ddItems.getSelectedItem() != null && !ddItems.getSelectedItem().toString().equalsIgnoreCase("select")) {
                String s[] = ddItems.getSelectedItem().toString().split("-");
                itId = Long.parseLong(s[s.length - 1]);
            } else {
                barcode = txtBarCode.getText().trim();
            }
            ItemsDao itmd = new ItemsDao();
            List<Items> ils = itmd.getItems(itId, null, null, barcode);
            if (!ils.isEmpty()) {
                items = ils.get(0);
                txtAddItemBrand.setText(items.getBrandId().getBrandName());
                txtAddItemCode.setText(items.getItemCode());
                txtAddItemName.setText(items.getItemName());
                txtPrice.setText(items.getPrice() + "");
                txtQuantity.setText("0");
                txtQuantity.setFocusable(true);
                pnlAddItems.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No Item Found");
            }
        }
    }

    public void addToCart() {
        boolean check = true;
        int qty = 0;
        if (txtQuantity.getText() == null || txtQuantity.getText().trim().length() == 0) {
            check = false;
            JOptionPane.showMessageDialog(this, "Quantity Requred");
        } else {
            try {
                qty = Integer.parseInt(txtQuantity.getText().toString());
                if (qty <= 0) {
                    check = false;
                    JOptionPane.showMessageDialog(this, "Quantity should be greater than 0");
                }
            } catch (Exception e) {
                check = false;
                JOptionPane.showMessageDialog(this, "Invalid Quantity");
            }
        }
        if (check) {
            SalesItems p = new SalesItems();
            p.setSrNo(srNo++);
            p.setItems(items);
            p.setQuantity(qty);
            p.setPrice(p.getItems().getPrice());
            p.setAmount(qty * p.getItems().getPrice());
            p.setDiscount(calDiscount(p.getAmount()));
            p.setTotalAmount(p.getAmount() - p.getDiscount());
            p.setItemNumber(txtSerialNo.getText());
            salesItems.add(p);
            pnlAddItems.setVisible(false);
            clearAddFields();
            showTableData();
        }
    }

    public double calDiscount(double amount) {
        double dis = 0;
        if (ddIDiscount.getSelectedItem() != null && !ddIDiscount.getSelectedItem().toString().equalsIgnoreCase("Select")) {
            dis = (amount / 100) * (Double.parseDouble(ddIDiscount.getSelectedItem().toString()));
        }
        return dis;
    }

