    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = cbmCustomer.getSelectedIndex();

        if (index > 0) {
            custName = customers.get(index - 1).getName();
        }
        if (dateFrom.getDate().after(dateTo.getDate())) {
            JOptionPane.showMessageDialog(pnReport, "From date before To date");
        } else {
            invoices = reportModel.getInvoiceDetails(custName, dateFrom.getDate(), dateTo.getDate());
            setInvoiceData();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

