    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRow() < invoices.size()) {
            if (jTable1.getSelectedRowCount() > 0) {
                reportModel.generateBills(invoices.get(jTable1.getSelectedRow()));
            } else {
                JOptionPane.showMessageDialog(pnReport, "Please select row for to view invoice details");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTable1.getSelectedRow() < invoices.size()) {
            if (jTable1.getSelectedRowCount() > 0) {
                String msg = reportModel.deleteInvoice(invoices.get(jTable1.getSelectedRow()));
                if (msg.equals("ok")) {
                    invoices = reportModel.getInvoiceDetails(custName, dateFrom.getDate(), dateTo.getDate());
                    setInvoiceData();
                }
            } else {
                JOptionPane.showMessageDialog(pnReport, "Please select row for Delete invoice details");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed
