    public void init(HomePanel homePanel) {
        this.homePanel = homePanel;
        customers = reportModel.getCustomerList();
        String[] sList = new String[customers.size() + 1];
        sList[0] = "----All----";
        for (int i = 0; i < customers.size(); i++) {
            Customer sp = customers.get(i);
            sList[i + 1] = sp.getName();
        }
        dateFrom.setDate(new Date());
        dateTo.setDate(new Date());
        cbmCustomer.setModel(new javax.swing.DefaultComboBoxModel(sList));
        invoices = new ArrayList<Invoice>();
        setInvoiceData();

    }

    public void setInvoiceData() {
        Object[][] data = new Object[invoices.size() + 1][6];
        double total = 0.0;
        double disc = 0.0;
        for (int i = 0; i < invoices.size(); i++) {
            Invoice cm = invoices.get(i);
            data[i][0] = (i + 1) + "";
            data[i][1] = cm.getInvoiceNumber();
            data[i][2] = cm.getCustname();
            data[i][3] = (cm.getInvoiceType() == 1) ? "CST Or VAT" : "Endorsement";
            data[i][4] = sd.format(cm.getInvoiceDate());

            data[i][5] = cm.getGrandTotal();
            disc += cm.getFinalDiscount();
            total += cm.getGrandTotal();
        }
        if (!invoices.isEmpty()) {
            data[invoices.size()][0] = "";
            data[invoices.size()][1] = "";
            data[invoices.size()][2] = "";
            data[invoices.size()][3] = "";
            data[invoices.size()][4] = "Total";

            data[invoices.size()][5] = total;
        }
        setTableDate(data);
    }

